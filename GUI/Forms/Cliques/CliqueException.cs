using System;
using System.Collections.Generic;
using System.Text;

namespace Network.Cliques
{
    [Serializable]
    public class CliqueException : Exception
    {
        public CliqueException() { }
        public CliqueException(string message) : base(message) { }
        public CliqueException(string message, Exception inner) : base(message, inner) { }
        protected CliqueException(
          System.Runtime.Serialization.SerializationInfo info,
          System.Runtime.Serialization.StreamingContext context)
            : base(info, context) { }
    }
}
